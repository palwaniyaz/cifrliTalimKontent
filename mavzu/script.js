window.onload = function () {
  const container = document.getElementById('accordionContainer');

  for (let i = 0; i < temaContent.length; i++) {
    const temaId = `tema${i + 1}`;      
    const accordion = document.createElement('div');
    accordion.className = "accordion";

    accordion.innerHTML = `
      <button class="accordion-button" onclick="toggleOnly('${temaId}')">
        Tema ${i + 1}: ${temaContent[i].title}
      </button>
      <div id="${temaId}" class="accordion-content">
        ${temaContent[i].content}
      </div>
    `;

    container.appendChild(accordion);
  }
};

function toggleOnly(id) {
  const sections = document.querySelectorAll('.accordion-content');
  sections.forEach(section => {
    if (section.id === id) {
      section.style.display = section.style.display === "block" ? "none" : "block";
    } else {
      section.style.display = "none";
    }
  });
}




// Rasmlarni dinamik yuklash uchun funksiya
function loadContentImages() {
  const images = document.querySelectorAll('img[data-src]');
  
  images.forEach(img => {
    // Lazy loading
    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          img.src = img.dataset.src;
          observer.unobserve(img);
        }
      });
    });
    
    observer.observe(img);
  });
}

// DOM yuklanganda ishga tushirish
document.addEventListener('DOMContentLoaded', function() {
  loadContentImages();
  
  // Kod bloklariga nusxa olish tugmasi qo'shish
  document.querySelectorAll('.code-example').forEach(example => {
    const copyBtn = document.createElement('button');
    copyBtn.className = 'copy-btn';
    copyBtn.textContent = 'Nusxa olish';
    
    copyBtn.addEventListener('click', () => {
      const code = example.querySelector('code').textContent;
      navigator.clipboard.writeText(code)
        .then(() => {
          copyBtn.textContent = 'Nusxa olindi!';
          setTimeout(() => {
            copyBtn.textContent = 'Nusxa olish';
          }, 2000);
        });
    });
    
    example.appendChild(copyBtn);
  });
});